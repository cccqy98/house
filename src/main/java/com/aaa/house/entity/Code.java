package com.aaa.house.entity;

public class Code {

    private int id;
    private int code_type;
    private int code_number;
    private String code_state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCode_type() {
        return code_type;
    }

    public void setCode_type(int code_type) {
        this.code_type = code_type;
    }

    public int getCode_number() {
        return code_number;
    }

    public void setCode_number(int code_number) {
        this.code_number = code_number;
    }

    public String getCode_state() {
        return code_state;
    }

    public void setCode_state(String code_state) {
        this.code_state = code_state;
    }
}
